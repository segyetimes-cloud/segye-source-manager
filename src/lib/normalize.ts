// 통합: 필드명 기반 정규화
// ─────────────────────────────────────────────────────────────────────────────
export function normalizeField(
  field: string,
  value: string
): { normalized: string; changed: boolean } {
  if (!value?.trim()) return { normalized: value, changed: false }

  switch (field) {
    case 'university':
    case 'graduate_school':
      return normalizeUniversity(value)
    case 'high_school':
      return normalizeHighSchool(value)
    case 'current_organization':
      return normalizeOrganization(value)
    case 'hometown_province':
      return normalizeProvince(value)
    case 'exam_batch':
      return normalizeExamBatch(value)
    default:
      return { normalized: value, changed: false }
  }
}

// ─────────────────────────────────────────────────────────────────────────────
// 취재원 객체 전체 정규화 (가져오기 최종 단계)
// ─────────────────────────────────────────────────────────────────────────────
export function normalizeSource(
  source: Record<string, string | string[]>
): { result: Record<string, string | string[]>; changes: Record<string, { from: string; to: string }> } {
  const result = { ...source }
  const changes: Record<string, { from: string; to: string }> = {}

  const NORMALIZABLE_FIELDS = [
    'university', 'graduate_school', 'high_school',
    'current_organization', 'hometown_province', 'exam_batch',
  ]

  for (const field of NORMALIZABLE_FIELDS) {
    const raw = source[field]
    if (typeof raw !== 'string' || !raw.trim()) continue
    const { normalized, changed } = normalizeField(field, raw)
    if (changed) {
      result[field] = normalized
      changes[field] = { from: raw, to: normalized }
    }
  }

  return { result, changes }
}
