// ─────────────────────────────────────────────────────────────────────────────
// 범용 정규화 함수
// ─────────────────────────────────────────────────────────────────────────────
function normalizeFromMap(
  value: string,
  aliasMap: [string[], string][],
  opts?: { fuzzyThreshold?: number; suffixRules?: [RegExp, string][] }
): { normalized: string; changed: boolean } {
  const v = value.trim()
  if (!v) return { normalized: v, changed: false }
  const vc = clean(v)

  // 1. 정확 일치 (대소문자·공백 무시)
  for (const [aliases, canonical] of aliasMap) {
    if (aliases.some(a => clean(a) === vc))
      return { normalized: canonical, changed: canonical !== v }
  }

  // 2. 접미사 규칙 적용 후 재검색
  if (opts?.suffixRules) {
    let modified = v
    for (const [re, replacement] of opts.suffixRules) {
      if (re.test(v)) { modified = v.replace(re, replacement); break }
    }
    if (modified !== v) {
      const mc = clean(modified)
      for (const [aliases, canonical] of aliasMap) {
        if (aliases.some(a => clean(a) === mc))
          return { normalized: canonical, changed: true }
      }
    }
  }

  // 3. 퍼지 매칭 (편집거리 기반)
  const threshold = opts?.fuzzyThreshold ?? 0.82
  if (vc.length >= 2) {
    let bestScore = 0
    let bestCanonical = ''
    for (const [aliases, canonical] of aliasMap) {
      for (const alias of aliases) {
        const ac = clean(alias)
        if (Math.abs(vc.length - ac.length) > 3) continue  // 길이 차이 너무 크면 스킵
        const score = similarity(vc, ac)
        if (score > bestScore) { bestScore = score; bestCanonical = canonical }
      }
    }
    if (bestScore >= threshold && bestCanonical)
      return { normalized: bestCanonical, changed: bestCanonical !== v }
  }

  return { normalized: v, changed: false }
}

