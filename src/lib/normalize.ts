/**
 * 고시/기수 정규화
 *
 * 처리 예시:
 *   "행시 35회"      → "행정고시 35회"
 *   "행정고시35기"   → "행정고시 35회"
 *   "35회 행시"      → "행정고시 35회"  (번호가 앞에 올 때)
 *   "사시45회"       → "사법고시 45회"
 *   "외시 15회"      → "외무고시 15회"
 *   "행정고시"       → "행정고시"       (기수 없을 때 이름만 정규화)
 */
export function normalizeExamBatch(value: string): { normalized: string; changed: boolean } {
  const v = value.trim()
  if (!v) return { normalized: v, changed: false }

  // ① 기수 번호 추출: "35회", "35기", "35차", "35" (숫자만 있는 경우도 처리)
  //    앞/뒤 위치 모두 허용
  const numRe = /(\d{1,3})\s*([회기차])/
  const numMatch = v.match(numRe)
  const batchNum = numMatch ? numMatch[1] : null   // 숫자만
  const batchUnit = numMatch ? numMatch[2] : null  // 회·기·차

  // 기수 부분을 제거한 시험명만 추출
  const nameOnly = v
    .replace(numRe, '')      // 숫자+단위 제거
    .replace(/^\s*[-·,]\s*/, '')
    .replace(/\s*[-·,]\s*$/, '')
    .trim()

  // ② 시험명 정규화 (별칭 테이블)
  const nameCleaned = clean(nameOnly)
  let canonicalName = ''

  for (const [aliases, canonical] of EXAM_NAME_ALIASES) {
    if (aliases.some(a => clean(a) === nameCleaned)) {
      canonicalName = canonical
      break
    }
  }

  // 정확 일치 없으면 퍼지 매칭
  if (!canonicalName && nameCleaned.length >= 2) {
    let bestScore = 0
    let bestCanonical = ''
    for (const [aliases, canonical] of EXAM_NAME_ALIASES) {
      for (const alias of aliases) {
        const ac = clean(alias)
        if (Math.abs(nameCleaned.length - ac.length) > 3) continue
        const score = similarity(nameCleaned, ac)
        if (score > bestScore) { bestScore = score; bestCanonical = canonical }
      }
    }
    if (bestScore >= 0.82) canonicalName = bestCanonical
  }

  // ③ 재조합
  if (!canonicalName) {
    // 시험명 정규화 불가 → 기수 단위만 통일 (기/차 → 회)
    if (batchUnit && batchUnit !== '회' && batchNum) {
      const rebuilt = v.replace(numRe, `${batchNum}회`)
      if (rebuilt !== v) return { normalized: rebuilt, changed: true }
    }
    return { normalized: v, changed: false }
  }

  // 기수 번호 있으면 "정식명 N회", 없으면 "정식명"
  const normalized = batchNum
    ? `${canonicalName} ${batchNum}회`
    : canonicalName

  return { normalized, changed: normalized !== v }
}

